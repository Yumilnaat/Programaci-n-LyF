% REGLAS
% Películas altamente recomendadas (calificación mayor o igual a 8.5)
altamente_recomendada(Titulo) :-
    pelicula(Titulo, _, _, _, Calificacion, _),
    Calificacion >= 8.5.

% Películas protagonizadas por un actor específico
peliculas_por_actor(Actor, Titulo) :-
    pelicula(Titulo, _, Actor, _, _, _).

% Películas estrenadas en un año específico
peliculas_por_ano(Anio, Titulo) :-
    pelicula(Titulo, _, _, _, _, Anio).

% Películas recomendadas para fans de un género específico con calificación mayor a 8.0
recomendada_para_fans_de_genero(Titulo, Genero) :-
    pelicula(Titulo, Generos, _, _, Calificacion, _),
    member(Genero, Generos),
    Calificacion > 8.0.

% Películas dirigidas por un director específico
peliculas_por_director(Director, Titulo) :-
    pelicula(Titulo, _, _, Director, _, _).

% Películas con un género en común (relación entre dos películas)
tienen_genero_en_comun(Titulo1, Titulo2, Genero) :-
    pelicula(Titulo1, Generos1, _, _, _, _),
    pelicula(Titulo2, Generos2, _, _, _, _),
    Titulo1 \= Titulo2,
    member(Genero, Generos1),
    member(Genero, Generos2).

% Películas destacadas del año (calificación mayor a 8 en cierto año)
peliculas_destacadas_por_ano(Anio, Titulo) :-
    pelicula(Titulo, _, _, _, Calificacion, Anio),
    Calificacion > 8.0.

% Películas clásicas (estrenadas antes del año 2000)
pelicula_clasica(Titulo) :-
    pelicula(Titulo, _, _, _, _, Anio),
    Anio < 2000.

% Películas recientes (últimos 5 años, ajusta si es necesario)
pelicula_reciente(Titulo) :-
    pelicula(Titulo, _, _, _, _, Anio),
    Anio >= 2020.

% Películas por década
peliculas_por_decada(Decada, Titulo) :-
    pelicula(Titulo, _, _, _, _, Anio),
    DecadaInferior is (Decada // 10) * 10,
    DecadaSuperior is DecadaInferior + 9,
    Anio >= DecadaInferior,
    Anio =< DecadaSuperior.